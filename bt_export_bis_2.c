#include "minishell.h"

char	set_separator(int *i, t_all *g_all)
{
	if (g_all->temp_str_1[*i] == '\'')
	{
		(*i)++;
		return ('\'');
	}
	else if (g_all->temp_str_1[*i] == '\"')
	{
		(*i)++;
		return ('\"');
	}
	else
		return ('=');
}
